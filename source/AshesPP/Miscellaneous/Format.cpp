	bool isDepthStencilFormat( VkFormat format )noexcept
	{
		return format == VkFormat::VK_FORMAT_D16_UNORM_S8_UINT
			|| format == VkFormat::VK_FORMAT_D24_UNORM_S8_UINT
			|| format == VkFormat::VK_FORMAT_D32_SFLOAT_S8_UINT;
	}

	bool isStencilFormat( VkFormat format )noexcept
	{
		return format == VkFormat::VK_FORMAT_S8_UINT;
	}

	bool isDepthFormat( VkFormat format )noexcept
	{
		return format == VkFormat::VK_FORMAT_D16_UNORM
			|| format == VkFormat::VK_FORMAT_X8_D24_UNORM_PACK32
			|| format == VkFormat::VK_FORMAT_D32_SFLOAT;
	}

	bool isBCFormat( VkFormat format )noexcept
	{
		return format >= VkFormat::VK_FORMAT_BC1_RGB_UNORM_BLOCK
			&& format <= VkFormat::VK_FORMAT_BC7_SRGB_BLOCK;
	}

	bool isETC2Format( VkFormat format )noexcept
	{
		return format >= VkFormat::VK_FORMAT_ETC2_R8G8B8_UNORM_BLOCK
			&& format <= VkFormat::VK_FORMAT_ETC2_R8G8B8A8_SRGB_BLOCK;
	}

	bool isEACFormat( VkFormat format )noexcept
	{
		return format >= VkFormat::VK_FORMAT_EAC_R11_UNORM_BLOCK
			&& format <= VkFormat::VK_FORMAT_EAC_R11G11_SNORM_BLOCK;
	}

	bool isASTCFormat( VkFormat format )noexcept
	{
		return format >= VkFormat::VK_FORMAT_ASTC_4x4_UNORM_BLOCK
			&& format <= VkFormat::VK_FORMAT_ASTC_12x12_SRGB_BLOCK;
	}

	bool isYCBCRFormat( VkFormat format )noexcept
	{
		return format >= VkFormat::VK_FORMAT_G8B8G8R8_422_UNORM
			&& format <= VkFormat::VK_FORMAT_G16_B16_R16_3PLANE_444_UNORM;
	}

	bool isPVRTCFormat( VkFormat format )noexcept
	{
		return format >= VkFormat::VK_FORMAT_PVRTC1_2BPP_UNORM_BLOCK_IMG
			&& format <= VkFormat::VK_FORMAT_PVRTC2_4BPP_SRGB_BLOCK_IMG;
	}

	bool isCompressedFormat( VkFormat format )noexcept
	{
		return isBCFormat( format )
			|| isETC2Format( format )
			|| isEACFormat( format )
			|| isASTCFormat( format )
			|| isYCBCRFormat( format )
			|| isPVRTCFormat( format );
	}

